from __future__ import absolute_import, division

from functools import partial
from werkzeug.local import LocalProxy, LocalStack


def _lookup_job_object(name):
    top = _job_ctx_stack.top
    if top is None:
        raise RuntimeError('working outside of job context')
    return getattr(top, name)


_job_ctx_stack = LocalStack()
job_logger = LocalProxy(partial(_lookup_job_object, 'logger'))
